let puntaje = 10;

console.log(puntaje++); //primero el valor y luego incrementa
console.log(++puntaje); //incrementa y luego primero el valor
console.log(puntaje--); //primero el valor y luego decrementa
console.log(--puntaje); //decrementa y luego primero el valor

puntaje += 10;

console.log(puntaje);